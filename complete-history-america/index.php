  <p><b>Preface</b></p>
  <p>For
    every production, CATCO hires a dramaturg to collect information about the play
    (its structure and setting), the playwright, and past productions.  This information, which is put together into
    an <i>Actors’ Book</i>, furnishes <i>a context for and is supplemental to</i> the
    research conducted by the artists involved in the production, including the
    director, actors, and costume, scenic, lighting, and sound designers,
    and props master.</p>
  <p>Dramaturgs
    prepare not only the <i>Actors’ Book</i>,
    but also <i>Play Notes </i>(or sometimes, <i>Stage Notes</i>), which is often a shorter
    version of the <i>Actors’ Book</i> and is
    made available to the theatre-going public. 
    For this CATCO production, this web site is combining both the <i>Actors’ Book</i> and <i>Play/Stage Notes</i>.</p>
  <p>This web site will be updated
    periodically throughout the rehearsal process.</p>
