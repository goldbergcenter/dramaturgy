<p><strong>CATCO --Quick Production FACTS</strong></p>
<p><em>The Grapes of Wrath</em> is CATCO's largest production to date.</p>
<p>Twenty-three actors, two musicians, two
  assistant stage managers (one who doubles as an actor), and two backstage
  runners join the Stage Manager and lighting and sound board operators for each
  performance.</p>
<p>The two musicians play guitar, fiddle, harp, and
  saw (which the musician learned for this production). Four actors join the
  musicians with other instruments: washtub base, harmonicas (2), and banjo.</p>
<p>Costumes: There are 90 different characters
  depicted on stage. On average, each actor has at least two costumes; one has
  seven costume changes.</p>
<p>Props: There are over 230 different props,
  ranging from barrels to crates to guns to bundles of clothing and eating
  utensils. One of the assistant stage managers "tracks" each prop and
  knows where it is at all times.</p>
<p>Specialists: In addition to the usual lighting
  and sound directors, CATCO hired a music director, a choreographer, and a
  fight-movement director (who doubles as an actor).</p>
<p>CATCO's budget for this production was about <strong>one-fifth</strong> the budget of the original Steppenwolf production.</p>
<p><strong>Geoff Nelson The Director's Approach</strong></p>
<p><img class="portrait" src="images/geoff-nelson.jpg" width="121" height="156" alt="Geoff Nelson">Geoff Nelson, Founder and Artistic Director of CATCO, handed out
  the following "approach" to all cast and crew members at the first rehearsal.</p>
<p>GRAPES is the
  biggest production that CATCO has ever staged biggest not only in the number of
  performers onstage (twenty-five) but more so in terms of story. We don't have a
  simple action here, with a single protagonist; we have instead a complex,
  interwoven series of actions involving an entire family as protagonist. The
  power of this play lays in this very <em>epic </em>quality: the audience becomes involved in <em>all </em>the separate plot lines of the Joad family. It is also <em>epic </em>because the Joads represent "The People," the working class
  family that has so little voice in America then and now. And it is <em>epic </em>because of the sheer cumulative
  power of twenty-five performers.</p>
<p>The idea of
  "The People" is at the heart of this production. Our premise is that
  the ensemble all twenty&#8209;five of you,
  representing "The People" tell us the story of the Joads. Some of you step out of the Ensemble to become
  individual Joads, others play a variety of the people they encounter on
  their Odyssey. Dead Joads disappeared Joads are recycled and come back to play other people.</p>
<p>[A note here on
  costuming and makeup: There's a lot of doubling in this show and we're
  certainly not trying to fool the audience. When the actor
  playing Connie Rivers returns to the stage as a striker the costume will make
  it clear that this is a different person. But it will be up to the actor
  to find a different physicality, vocalization, temperament, etc.]</p>
<p>This idea of
  "The People" animates the casting and musical assignments as well.
  I'd like to spread the telling of the story among many performers. Our skilled
  musicians will necessarily carry the bulk of the music, but we are also looking
  for places where a few of the actors who play instruments or sing take on a
  number. I have also replaced the largely male narrators in the original script
  with purely female narrators. There's a wonderful image in the book: the men squatting in a circle to make big
  decisions, while the women stand with the children on the periphery waiting. At
  the end of this play, though, the women clearly provide much of the strength
  that keeps us going.</p>
<p>This is not the
  Steppenwolf Production. We don't have the resources, nor the space, to fly scenery nor to store a real truck. Galati's original
  production used real fire, real tanks of water, real rain and we <em>could </em>probably do that. But instead we
  have chosen a more <em>theatrical </em>approach.</p>
<p>One of the
  interesting elements in the script is the idea of <em>transformation </em>how the band's music becomes the sound of the engine
  stalling, how the jaw harp becomes a heartbeat, how the square dancers become
  the strikers, etc. In our production this concept will guide a lot of the
  staging. Music or live sound effects will create the effect of mimed shoveling
  (no real dirt) when Grandpa is buried; actors and long pieces of cloth will
  create the Colorado River; the barn at the end will be created by the Ensemble
  holding planks, etc. We don't have all the details worked out, but my
  expectation is that we will use the Ensemble and music and live sound effects
  more than the Steppenwolf Production.</p>
<p>The settings will
  also be created out of a lot of the same units that will be re-used in
  different ways, as well as with new scenic pieces. The truck as Dan will
  explain is created largely out of non-truck parts--again, the idea of <em>transformation.</em></p>
<p>The scene changes
  in this play will be big production numbers, accomplished by the Ensemble
  rather than by technology. These scene changes are usually accompanied by songs
  or text. There are also a number of crowd scenes where the Ensemble will be
  actively involved in creating distinct, believable characters who don't speak and aren't listed in the script! When I say
  that the cumulative impact of twenty&#8209;five actors will help create that <em>epic </em>feeling
  in the audience, it is because the Ensemble will be such a strong presence
  onstage from first to last. (In the Steppenwolf production, the ensemble and
  the music petered out as the story progressed. Not here.)</p>
<p>One staging note:
  we will be using the full auditorium. Moments will be staged out in the house.
  I think it's important for the audience to enhance that feeling of scale and
  space.</p>
<p>And a few words
  on some central acting issues....</p>
<p>If we play the Joads as victims, the production will fail. The goal is not
  for the audience to feel sorry for this family. The goal is for the audience to <em>admire </em>them, to be in awe of their
  strength and generosity and perseverance.</p>
<p>The important
  thing is not what happens to the Joads, but how they
  respond, how they change as a result of what they experience. They are not
  whiners. They are a tough, hard-working, proud (sometimes too proud) people who
  put a very, very high value on self-reliance.</p>
<p>At heart, this
  play is about values about one family and how its values (self-reliance,
  family, religion) change. How <em>community, </em>how responsibility for your fellow man becomes crucial
  to them. How "I" becomes "We." It's a movement from
  ignorance to knowledge. It's also the recognition of how one gains power over
  one's own life from being part of a powerful group. In the end, self-reliance
  is an illusion. And family values are only significant if they are supported by
  community values.</p>
<p>Be careful of
  playing sentiment. Rarely do these people have the time, or inclination, to sit
  around and feel sorry for themselves. This is THE GRAPES OF <em>WRATH, </em>remember, not ANATOMY OF
  MELANCHOLY. These are also people who distrust sentiment as a sign of weakness:
  if you let yourself be emotional you're done for, you won't have the strength
  to fight on </p>
<p>The humor in the
  play is crucial. It counterpoints all the hard stuff, which would get
  unbearable without it.</p>
<p>For the Ensemble:
  don't play everyone as villains. The worst people the Joads encounter are rarely conscious of who they are and what they do. They think
  they are right, that they are justified. They are driven by fear, or need, or
  mistaken notions. Find the positive side of these characters it will make their
  actions all the harder</p>
<p>And finally and
  most importantly: be honest. You are speaking for people who had (and have)
  little voice in our culture.</p>
<p><strong>Scenic Design</strong></p>
<p><strong>Dan Gray</strong>, Resident Scenic
  Designer and Assistant Professor<br />
  Department of Theatre, The Ohio State University</p>
<p>Transformation, adaptation, and determination
  were essential to the Joad's survival in a harsh
  world.  These qualities drove and defined the scenic approach to our
  production.</p>
<p>Conceptually, my initial scenic-design response
  to <em>The Grapes of Wrath</em> was driven by some very practical concerns.
  How would we create a space that would support a cast of 25 people, a truck to
  transport 13 of them to California, and a sense of the expansive vistas of the Joad's journey on a relatively small stage?  How could
  we show the desperation and transformation of these characters in a way that
  reflected the realities they encountered day after day?  The answer to
  these and other questions came from the texts--John Steinbeck's novel and the
  Frank Galati-Steppenwolf adaptation.</p>
<p>The novel is a story of real characters (who
  have all seen better days) told in simple, direct language.  The stage
  environment would have to reflect this straightforward approach. Honest
  materials, worn and blown raw by the elements, would frame this world.
  The stage would be a wide open space with a series of levels to create a varied
  palette for composing our performers. Different locations would be suggested by
  elements found within the environment and manipulated by the actors.  We
  would make no pretense of hiding the theatrical nature of our communication
  with the audience.  Large scenic changes made by unseen hands and other
  "theatre tricks" had no place in this production.</p>
<p>The famous "Joad Truck" would also be created in this manner.  Practically, we had no
  room for even a scaled down version of a Hudson Super Six on (or off!)
  stage.  We needed to find a way of suggesting this emblematic vision of
  migration in a way that was consistent with the approach we had embraced.
  In my research for the production I saw many video and still images of Okies fleeing to California with their belongings strapped
  all over their vehicles.  I was struck by the idea that in many cases you
  couldn't really see the vehicle for all the stuff that surrounded it.
  This would prove to be the key to our truck.  We would create the
  suggestive silhouette of a period Hudson on a roving platform with items from
  the destroyed Joad place.  A round top trunk
  formed the engine bonnet; a bench made the seat; fence pieces defined the truck
  bed; a headboard separated the cab from the bed of the converted car-into-a
  truck. Most of these items could then be used as props in other scenes as
  needed. Thus, the truck reflected the themes of transformation, adaptation, and
  determination.</p>
<p><strong>Costume Design</strong></p>
<p><strong>Cindy Turnbull</strong>, Assistant
  Professor of Theatre and Resident Costume Designer<br />
  Department of Theatre, Denison University</p>
<p>As Costume Designer for <em>The Grapes of Wrath</em> I have had many
  challenges. There are over 90 characters acting in diverse locations from
  Oklahoma to New Mexico to Arizona and California. I am focusing on the
  "clothes" of the period and making these garments look worn, lived
  in, and authentic to the 1930's and the characters. I am also working with
  textures of all kinds, combinations, and different colors. All of this must be
  created within the time, labor, and money constraints that all theatrical
  artists work within.</p>
<p>My general process in designing is:</p>
<p>Read the script several times</p>
<p>Research using period sources</p>
<p>Prepare character analysis of all characters within the show</p>
<p>Draw preliminary sketches and get feedback from the director</p>
<p>Make changes to the sketches</p>
<p>Shop for fabric for garments we are constructing</p>
<p>Shop for garments at thrift stores</p>
<p>Fit the actors in their costumes</p>
<p>Build and alter garments</p>
<p>Laura Dana is Costume Shop Supervisor at CATCO and is doing a
  wonderful job overseeing the building and construction of the costumes. She
  also organizes the fast changes and running of the show as it goes into dress
  rehearsals.</p>
<p>In this production process, I am also serving as the dyer/painter.
  To achieve the worn, tattered, and dirty look of some of the clothes, I am
  painting them with dye and over dying some of them to create a faded look. I
  had to purchase new denim pants for some of the men and I have bleached and
  faded them so that they look old and lived-in.  After the dyeing and painting is done sandpaper
  and cheese graters are used to fray the fibers. This also adds a
  three-dimensional texture to the garments.  Other textures I am working
  with include fabrics with stripes, plaids, and prints and different weights and
  drapes of fabrics. This variation creates a visual variety and an earthy
  quality for the characters. Sometimes there are even stripes and prints used
  within the same costume.</p>
<p>Creating the world of the characters and clothes that make sense
  in that world is my goal. But this production is not reality, not real life; it
  is its own theatrical and artistic statement based on the visions of the
  various artists working on it. I want the clothes to say something about the
  characters that might help the audience understand the story a little better.
  This is subtlety at its finest. No sequins or metallic fabrics are found in
  this show. I hope all audience members discover themselves transported back in
  time over sixty years ago and that, without consciously noticing, the costumes
  blend into that world created on stage.</p>
<p><strong>Musical Direction</strong></p>
<p><strong>Tammy Hansan</strong></p>
<p>Since the founding of our country, music has
  provided our society with reasons to believe.  Music continues to carry
  the country from the protest songs during the civil rights and antiwar
  movements through the global activism we see today.  Civil rights activist
  and musician Harry Belafonte said, " Wherever I go in
  the world and I look for forces that are struggling against injustice, I always
  seem to find somebody with a guitar and a voice and they're always singing to
  something that inspires the community, no matter how ravaged that community may
  be."  The Joads came from a
  community ravaged by Mother Nature and big corporations; the same community
  that triggered Woody Guthrie to create songs of desperation, songs of hope and
  songs of promise.<br />
   <br />
  I chose the music used in CATCO's production of <em>The Grapes of Wrath</em> to
  do several things.  First, generally it reflects the attitudes and
  emotions of the Joads. For example, "The
  Waltz," heard throughout the play, serves as Ma's memories of the past,
