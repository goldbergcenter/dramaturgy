<h1><a name="Migrants_in_Ohio"></a>Migrants in Ohio</h1>
<p>The plight of the Joads as depicted in <em>The
  Grapes of Wrath</em> was a universal one. Only the particular crops and the background of migrants in other areas
  differed. The economic forces behind
  agriculture in Ohio were not all that different from those in California,
  although sugar beets and onions comprised the major crops in Ohio for which
  casual workers were needed (for weeding several times a year and for the final
  harvest). A handful of large-scale
  agribusinesses dominated the state's agriculture and the small-scale farmer
  found it difficult to compete. The
  small-scale farmers could not even match the below-poverty wages that the
  large-scale farmers offered to the migrant laborers. The farmers complained to the state
  government, but received little help. There appeared to be among the small-scale Ohio farmers a greater sense
  of concern for the plight of the migrants, but they did not have the resources
  to furnish a decent, living wage. </p>
<p>In Ohio, migrants from
  Appalachia and from Mexico dominated the farm laboring community. Those from Appalachia, particularly from
  Kentucky, were very similar to the Joads: religious, prideful, family-oriented,
  hard-working. Those Ohioans who knew of
  the migrants treated them, like Californians did the Okies,
  with disdain and misunderstanding. Ohioans saw migrants as a burden on local relief rolls, as choosing
  their existence out of ignorance. When
  migrants remained in the area, crime rates did increase. And the migrants tended to bring their
  inter-family feuds with them, thus sparking an increase in violence when they
  were out of work. For the most part,
  however, the Ohio migrants were unseen to most Ohioans until a strike caught
  the attention of the state's press. The
  strike began in June 1934 in Hardin County, in the marshes that provided good
  soil for onions. Local law enforcement
  officials joined private citizens and the Ohio National Guard to break the
  strike. Riots occurred in Alger; local
  citizens captured and beat the leader of the strikers. Eventually, strike-breakers left the area and
  the migrants returned to the fields, but without having won any of their
  demands for better pay and housing conditions. After that was settled, however, the migrants became invisible once
  again to most Ohioans. The onion fields
  proved less profitable, and Mexican migrants began to supplant the Appalachian
  workers. The story of the Mexicans,
  alas, repeated that of the Appalachians.</p>
