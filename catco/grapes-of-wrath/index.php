<h1><a name=Introduction></a><strong>Introduction</strong></h1>
<p>The historian Alan Brinkley (in a review of Steppenwolf's New
  York production) has written this about <em>The
  Grapes of Wrath</em>: "... Steinbeck's
  novel is more than an intriguing period piece. Despite its many flaws it speaks to modern audiences, as it did to
  audiences in the 1930's, by evoking one of America's most powerful and
  cherished images of itself. It suggests
  that running like a river beneath the surface of the nation's cold, hard,
  individualistic culture lies the spirit of Ma Joad, a spirit of 'fambly' and
  community that, once tapped, might redeem us all." ("Why Steinbeck's Okies Speak to Us Today," <em>New York Times</em>,
  18 March 1990, Arts &amp; Leisure section, p. 13). The novel that had challenged a nation's soul
  late in the Depression decade still resonates in American hearts and minds over
  a half century later. </p>
<p>CATCO put together the following overviews, analyses, and
  references to help the actors, technicians, and audience more fully appreciate
  CATCO's production of Frank Galati's adaptation of John Steinbeck's <em>The Grapes of Wrath</em>. We look first at the nature of American
  culture in the 1930s and how John Steinbeck and the documentary tradition
  interacted with that culture. Then we
  present a brief biography of John Steinbeck, a discussion of the novel's
  history and structure, and analyses of the novel's relation to history,
  particularly the Dust Bowl, migrant patterns, and migrant life. A brief section on the film follows. A longer section on Steppenwolf's adaptation
  includes discussion of Frank Galati, the production's development, a brief
  synopsis of the adaptation, critics' views, and a list of other productions by
  Galati. There will follow a section on
  how CATCO decided to approach the production. Finally, sections on what happened to the Okies and migrants in Ohio in the 1930s precede the final section, a list of
  references and suggestions for further research. All of this information is intended to
  present a glimpse into the creative process and to enrich the audience members'
  experience of one of America's most significant play adaptations.</p>
