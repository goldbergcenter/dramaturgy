<h1><a name="Questions_to_Consider"></a>Questions to Consider</h1>
<p>How does the concept of the American Dream lace
  the novel and the play? Why have the Joads lost the American Dream? Indeed, is "loss" the correct
  description? Will they ever (re)discover
  or realize the dream?</p>
<p> Does Steinbeck believe that capitalism has
  failed the Joads, or that the Joads have failed capitalism? </p>
<p> How does the land figure in the
  novel? in the play? Do the individuals and institutions have the proper respect for the land
  that Steinbeck thinks they should? Can
  they develop such respect?</p>
<p> Similarly, is the notion of capitalism
  (especially individualism and land ownership) incompatible with conservation
  and concern for ecological balance? </p>
<p> Note the relationships between
  genders: how the men and women
  characterize their plight; the solutions offered; and the attempts to realize
  appropriate responses. Do the women or
  the men have the answers?</p>
<p> Does Steinbeck offer clear cut solutions
  to the problems presented? Who or what
  is responsible for the plight of the migrants and who or what should respond to
  their plight?</p>
