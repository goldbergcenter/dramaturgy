  understanding an adaptation of an historical novel to the stage.</p>
<p>------------------------------------------------------------------------------------------------------------------</p>
<p>Jonathan Abarbanel, "The Chicago Troupe Shoulders a National
  Epic and Heads West," <em>American Theatre</em> (June 1989), 22-28.</p>
<p>There was some criticism of the cost of the
  original Chicago production ($500,000), which an AT&amp;T foundation
  bankrolled. "The argument goes that
  accepting corporate money was a violation of Steinbeck's political
  point-of-view in a work in which bosses, banks, and corporate entities are the
  enemies."</p>
<p><em>These critics did not understand
  Steinbeck. He was not
  anti-capitalist. In fact, while writing
  the novel he was very concerned with his own personal finances, was involved in
  the negotiations surrounding his changing publishers, and buying a new
  ranch. Steinbeck refused several
  opportunities to go on the radio or lend his name to causes supporting the
  migrants' plight. Steinbeck did not want
  to eliminate the corporations; he wanted them to act more responsibly. Thus, the corporate sponsorship of Galati's
  adaptation reflects a kind of corporate responsibility Steinbeck would have
  supported. Indeed, his widow strongly
  supported the production, and introduced the PBS broadcast of the play.</em></p>
