<p>Many of the Okies were extremely
  religious--holy rollers and Pentecostals particularly. While Steinbeck saw the Okies as being very strong and determined and, indeed, the hope for California's
  future, he could not abide their religious fervor. In fact, this religious fervor conflicted
  with the Democratic Party ideologies behind New Deal programs, especially in
  the government migrant camps. Thus you
  have the scene at Weedpatch (Elizabeth Sandry character). There is in an historical sense, then, a direct thread from the Okies back to the Second Ku Klux Klan of the
  1920s (Oklahoma's government was heavily influenced by the Klan in the
  early-to-mid 1920s) and forward to the Far Right movement of present days. Their religion enabled them to put one foot
  in front of the other and to focus intently on the task at hand--getting work.</p>
