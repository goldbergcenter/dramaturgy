<h1><a name="The_Documentary_Movement_in_the_1930s"></a>The Documentary Movement in the 1930s</h1>
<p>Steinbeck had intended the novel to "document" the plight of
  American migrants. He saw himself--and
  in fact he was--part of the documentary movement of the 1930s. The documentary approach began in the
  mid-to-late nineteenth century in England; in the U.S., it was Jacob Riis, with
  his publication of <em>How the Other Half
  Lives</em> (1903) who publicized the style in America. The muckrakers, such as Upton Sinclair, who
  published <em>The Jungle</em> in 1906, are
  also ancestors of the 1930s documentary style. In one ironic sense, <em>The Grapes of
  Wrath</em> grew out of Steinbeck's failure to follow this tradition: He had intended to publish a story with
  photos of migrant life in California for <em>Life</em> magazine; but, to him, the facts he uncovered and the events he had experienced
  could not be adequately conveyed in a text-photo journalism style; it required
  a long novel. But as many have noted,
  the novel included many elements of the documentary style.</p>
<p>What is the documentary style? Many in the 1930s saw the documentary as "propaganda," which in a sense
  it was, for it advocated a particular view of a particular situation. But the movement was more complicated than
  that; it included a sense that "art" was a legitimate vehicle for exposing the
  problems of the times. As William Stott
  points out in his book, <em>Documentary
  Expression in Thirties America</em>, "documentary," "[L]ike 'document,' from which it derives, ... has <em>two</em> meanings, ... These meanings are not
  mutually exclusive ... The first ... has been defined as 'presenting facts
  objectively and without editorializing and inserting fictional matter, ....'"
  (5-6) A second
  kind of document, the "human document," is not objective but personal. "... a human
  document carries and communicates feeling, the raw material of drama. Such a document gives some information that
  would be found in an impersonal document." (7) "We understand a historical document intellectually, but we understand a
  human document emotionally. In the
  second kind of document, as in documentary and the thirties' documentary
  movement as a whole, feeling comes first." (8)</p>
<p>Stott points out that Warren Susman "affirmed that 'the whole idea of documentary--not with words alone but with
  sight and sound--makes it possible to see, know, and feel the details of life,
  to feel oneself part of some other's experience.' [Susman implied
  that] one knows another's life because one feels it; one is informed--one sees
  it--through one's feelings. The
  practitioners of the documentary genre in the thirties realized, if dimly, the
  same thing: emotion counted more than
  fact." (8-9) Clearly,
  with his focus on the Joad family, Steinbeck employs
  "a human document"; but, in the intercalary chapters, Steinbeck also employed
  facts about the economy and the migrants that he had gathered during the
  decade.</p>
<p>Pare Lorentz, who was a friend of Steinbeck's, and who hired
  Steinbeck to work on some of his films, reigns as the most important
  documentary filmmaker of the period. Yet, some do not agree with that assessment; instead, they argue that
  Lorentz was a propagandist because he worked for the New Deal administration. His most famous productions (<em>The River</em> and <em>The Plow That Broke the Plains</em>, both produced in 1936 under the
  auspices of the RA) included both facts and emotions; but they also
  included--against Lorentz' request--sections that praised the New Deal programs
  designed to attack the consequences of the Depression.</p>
<p>Another form of documentary prominent in the 1930s, which drew
  from the legacy of Jacob Riis, was the photograph-text book. Many photographers, many employed by New Dea agencies, made their artistic reputations chronicling
  the common man in America. They included
  women (Dorothea Lange, Margaret Bourke-White) and men (James Agee and Walker
  Evans, of <em>Let Us Now Praise Famous Men</em>,
  1941).</p>
<p>John Steinbeck was <em>of</em> these cultural forces, if not always <em>in</em> them.</p>
