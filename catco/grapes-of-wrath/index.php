<p><strong>Migrant Life</strong> </p>
<p>If one has read the novel and done some research on the lives
  of migrants in America, one is struck by how accurate Steinbeck's depictions
  really are (surely a tribute, in part, to his use of Tom Collins'
  information). [For an accessible
  internet site at the Library of Congress, see Selected References/Suggestions
  for Further Reading.] Migrants' lives
  were ones of constant travel, for they needed to look for work and often drive
  some distance to the work that they found. Not surprisingly, information gathered by government researchers in the
  late 1930s and early 1940s reveal a complex community. Some migrants had succumbed to the hazards
  and travails of the road and lost much of their dignity. Many did not. These insisted on staying clean, when possible, and wearing clean
  clothes when engaging in various recreational activities, such as singing,
  playing music, and dancing. Californians, however, who had already established a legally-based
  segregated world separating Anglos from Asians, and who also discriminated
  against Hispanics and African Americans, tended to segregate themselves from
  the Okies; one movie theater sign instructed,
  "Negroes and Okies upstairs."</p>

