<h1><a name="The_Play"></a>The Play</h1>
<p>Frank Galati's work has usually focused on the visual, and often included music; his <em>Grapes of Wrath</em> is no exception. In any play adaptation of a novel, the adaptor must make choices; not
  all material and themes can be replicated on the stage; yet, the adapter should
  stay true to the author's intentions. Most critics believe Galati did this. He chose to fuse the scenes in Oklahoma and on Route 66 into Act I; all
  of Act II occurs in California; this division reflects Steinbeck' choices in
  structuring the novel. While he
  underplays the theme of the land in Act I, Galati does emphasize the various
  people the Joads encountered (although not all of
  them) and other themes from the novel (individualism, community). He does so through dialogue and music; the
  latter conveys themes underplayed in the dialogue and often compresses several
  themes together.</p>
<p><strong>The Adaptor: Frank Galati</strong></p>
<p><img class="portrait" src="images/frank-galati.jpg" width="110" height="125" alt="Frank Galati">
  Frank Galati first thought about
  adapting <em>The Grapes of Wrath</em> to the
  stage in the 1970s. He envisioned a
  three-night extravaganza similar to England's <em>Nicholas Nickleby</em>. Once he hooked up with Steppenwolf in the
  1980s (he was still affiliated with the Goodman Theatre), he revisited the
  concept, but made a false step, thinking in terms of a deconstruction of the
  novel that might incorporate contemporary images, be Brechtian-didactic,
  and use images from the 1940 John Ford film. "We had some ideas about Svoboda-like screens, and most definitely a
  turntable--a huge disc of earth, and the truck moved on it. We were very, very smitten by that idea. ... A terrible idea." Then, during a two-week vacation in January 1987, Galati completed most
  of the script. [All quotations in this
  section come from Jonathan Abarbanel, "The Chicago
  Troupe Shoulders a National Epic and Heads West," <em>American Theatre</em> (June 1989), 22-28.]</p>
<p>At the center of his vision
  were the elements fire, water and rain," which together "offered both symbolic
  and architectural symmetry." His
  dramaturgical solution to the interchapters was to
  create musical interludes; Galati "wrote" the lyrics, and Michael Smith created
  the music.</p>
<p>Galati's principle arch was
  straightforward: "Tom is the hero, and
  the result of the conflict is Tom's revelation of Casy's gospel or message. This is offered to
  Tom in the very first scene of the novel, but Tom doesn't get it until he makes
  his last appearance in the story. The
  catalyst is Casy's death--that's the crisis, and it
  leads inexorably to Tom's revelation. So, I started there."</p>
<p>"The locations of the fire and water traps and
  the limited range of motion of the Joad's truck
  pre-ordained the placement of many key scenes. Moving on a track, the truck could pivot on its own axis and move from
  wing to wing, but it could not move upstage or downstage or at a diagonal."</p>
<p>Galati: "We had to figure out how many times the truck was going to stop, and
  where exactly it was going to stop. Kevin [Rigdon--designer] and I went very
  carefully through the show many times to accomplish the sensual images we
  needed. We still wanted earth, air, fire
  and water. That was our governing
  principle."</p>
<p>Galati added different narrators to enhance
  the "vox populi"
  effect. He added humor (especially
  between Grandpa and Al) and the second act square dance to lighten the "awful
  lot of bad news" in the story.</p>
<p> <strong>Play Synopsis</strong></p>
<p> Galati
  put the first one-half of the novel establishing the reasons the Joads were leaving, packing up, and the road trip to
  California in in Act I. All of Act II takes place in California, just
  as in the last one-half of the novel. </p>
<p>In Act I Tom meets up with Casy, and
  for a brief time, Muley,
  reunites with the family, and is informed of the decision to leave. Having bought a Hudson Super Six and
  converted it into a truck, the family packs up and begins the trek along the
  migrants' highway, Route 66. Along the
  way, the family buries Granpa, dreams about California,
  meets The Man Going Back, who suggests that their dreams will be dashed, learn
  the lessons of the road and various camps, and finally arrive in the promised
  land (with only $40), where they bury Grandma.</p>
<p>In Act II, the Joads experience a Hooverville in California, where Floyd Knowles again
  indicates that their dreams will not be fulfilled and Casy takes the blame for a confrontation between Tom and a deputy. They transfer to one of the government camps, Weedpatch, where they learn that working together as
  a community can be successful. Lack of
  work in the area, however, forces them to move on to the Hooper Ranch and a
  private camp. Tom discovers here that Casy has joined a group of migrants on strike. A horrific fight breaks out when a private
  police force attacks the strikers. Tom
  kills the man who killed Casy and he must say goodbye
  to Ma and the family. The family--what
  is left of it--ends up in a boxcar camp, where flooding rains ruin the car and
  force them to move to a barn on higher ground. Rose of Sharon bears a stillborn baby and, in
  the play's (and the novel's) final scene, presents Steinbeck's hope that the
  strong, moral fiber of the migrants will make the future better than the
  present.</p>
<p><strong>Production History</strong></p>
<p> At
  least two other adaptations of the novel have been produced. One was developed by Terence Shank at L.A.'s
  Colony at Studio Theatre Playhouse, 1980; this production was featured at
  Humana Festival of new plays. Another,
  adapted by Peter Whitebrook, premiered at the
  Edinburgh International Festival Fringe in late summer 1987. </p>
<p>Steppenwolf Theatre Company of Chicago first presented <em>The Grapes of Wrath</em> at the Royal-George
  Theatre in Chicago in September 1988. La
  Jolla Playhouse in La Jolla, California, produced the play in May 1989 and the
  Royal National Theatre of Great Britain produced it in London in June
  1989. The Schubert Organization,
  Steppenwolf Theatre Company, and Suntory International Corporation produced the
  Broadway stage version at the Cort Theatre in New
  York City in March 1990. Galati won a
  Tony for his direction and the adaptation won the Tony for Best Play.</p>
<p>Other productions of Galati's adaptation include The Theatre
  Academy of Los Angeles City College, in collaboration with the City Playhouse,
  a then-new resident professional theatre company, 1992; Washington Shakespeare
  Company, Gunston Arts Center in Arlington,
  Washington, 1992; the Cameri Theatre in Tel Aviv,
  Israel, in 1993, in Hebrew ("a colossal flop"); Cal State Fullerton in 1993;
  another English production at the Sheffield Crucible in 1994; Orange Coast
  College in Costa Mesa, CA, in 1994; California Lutheran University in 1994;
  Mountain View Center for the Performing Arts (CA) in 2000; and, the Arkansas
  Repertory Theatre, Little Rock, AK, 2000.</p>
