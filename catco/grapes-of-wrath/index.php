<p>Steinbeck <em>believed</em> in
  the migrants. He saw them as the future
  of California: "The Californian doesn't
  know what he does want. The Oklahoman
  knows just exactly what he wants. He
  wants a piece of land. And he goes after
  it and gets it." And, "Their coming here
  now is going to change things almost as much as did the coming of the first American
  settlers." [quoted in DeMott, <em>Working Days</em>, from Louis Walther,
  "Oklahomans Steinbeck's Theme," <em>San Jose
  Mercury Herald</em>, 8 January 1938] While he was embarrassed by the treatment they received and worried that
  the failure to change the circumstances would undermine California's economy,
  he also saw in the migrants themselves a resoluteness that would eventually
  save California.</p>
<p>Steinbeck consistently refused to go beyond the book to promote
  actively the migrants' cause. He was not
  "political" in the sense that he leant his name to various causes or even led
  rallies (one exception was the "Steinbeck Committee," which supported strikers,
  but Steinbeck himself did not actively promote this organization). Steinbeck did offer a solution to the migrant
  problem in California: The state should
  establish a Migrant Labor Board, which would regulate the number of migrants,
  oversee the camps, and during the off-season provide the migrants some land on
  which they could grow their own vegetables. Hardly a radical plan, it reflected Steinbeck's basic conservatism: He believed in the migrants, but he also
  believed in "America." He understood
  that the economic changes occurring meant that the large-scale corporations
  were not going to go away and that ways must be found to make the corporation
  act more morally. The novel implies that
  the strength of family and communities coming together ("I to we") might work, but there is no real conviction behind it.</p>
