<h1><a name="What_Happened_to_Okies"></a>What Happened to the Okies?</h1>
<p>According to Jules Loh,
  a California journalist in the early 1990s, Ma Joad's prediction that the Okies would simply go on actually
  understated their impact on California. For example, Dale Scales (who was 57 in 1992), and who was a baby in
  1936 when his family of six arrived in Bakersfield, owned 1800 acres, which he
  leased out, and made "his living trading in huge tracts of farmland for
  corporate investors, lives in the highest hill in Bakersfield, keeps a $45,000
  custom car in the garage and golfs at the country
  club." Perhaps as many
  as one-eighth of California's population in 1992 could trace ancestry to the Okies--about 3.75 million. Many had stayed on in California when
  mobilization for World War II presented good job opportunities.</p>
<p>A native Californian who
  witnessed the Okie invasion in the 1930s, Doris
  Weddell, "watched her native valley [San Joaquin Valley] ... steadily take on
  manners and folkways of the rural Southwest that weren't there before."  "The churches illustrate one obvious change.
  ... When I was a girl in Modesto there
  were five -- Methodist, Baptist, Catholic, Presbyterian and Episcopalian. Now only the Catholic Church is
  prominent. It serves the Mexicans,
  today's Okies. Now we have Pentecostals, Evangelicals, various fundamentalist
  congregations, and there are scads of them all over the valley. What used to be the main-line churches are
  almost insignificant." The infamous Weedpatch government camp of the novel (actually titled
  Arvin Labor Camp) was still in operation in 1992. Established in 1937 by the FSA with concrete
  floors and tents, it had improved by the 1990s to include two-bedroom plywood
  houses that sheltered 130 families; signs were in Spanish. [Jules Loh,
  "California's Okies," 15 October 1992, <em>The Press-Enterprise</em>, E-11.]</p>
<p>For other information on
  the Okies today, see Selected References/Suggestions
  for Further Reading. Dan Morgan's book,
  especially, underscores the religious aspect of Okie lives.</p>
