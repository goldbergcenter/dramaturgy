<p>The
  drought of the 1930s began early in the decade in the East--Maryland and
  Virginia. By 1931, portions of Montana
  and the Dakotas had been hit hard. The
  drought then spread from the east in a band southwest to Missouri and Arkansas
  and continued westward throughout the decade. In 1934, the financial loss attributed to the drought equaled one-half
  the cost of U.S. participation in World War I. By the mid-1930s, the southern plains were the most severely hit; the
  rains did not return there until 1941. May of 1934 saw the worst of the dust storms--350 million tons of dirt
  in the air, with 12 million tons falling on Chicago. "Blackouts" lasted from one hour to three and
  one-half days; one could not see one's hands in front of him during the worst
  of these storms. Inhabitants suffered
  from "dust pneumonia" and other respiratory infections. Houses could not be kept clean; the dust came
  in through the smallest of cracks.</p>
<p>The Dust Bowl consisted of 100 million acres, 500 miles North
  to South, 300 miles East to West, and covering land in western Kansas, the
  panhandle of Oklahoma, two-thirds of the Texas Panhandle, northeast New Mexico,
  and southeast Colorado. The Dust Bowl
  existed because human intervention (Muley: "We never should have broke her up")
  exacerbated natural cycles of drought. In the area of the Dust Bowl, mechanization of cotton production had
  advanced further than anywhere else in the Plains states. Less than one-half of the land was owned by
  residents of the state; 40 percent of the farmers were tenants or share
  croppers.</p>
<p><strong>Agriculture in California</strong> </p>
<p>While technology exacerbated the effects of the drought in the
  1930s, technology made possible the development of the lush valleys and fruit
  groves in California. Water--and the
  transportation of water--dominated California agriculture. Dams on rivers, especially the Colorado
  River, enabled Californians to capture water; irrigation ditches and canals
  enabled the farmers to transport that water to their farms. Such technology (which in hindsight probably
  did more ecological damage than was necessary) was expensive. And California had higher taxes than other
  farming states. Thus, high costs of
  operations dominated agriculture in California. High costs can be mitigated through combining operations and that is
  exactly what happened in California. Ten
  percent of the farms produced over 50 percent of the crops. This concentrated power in the hands of a
  few. The large-scale farmers controlled
  the California Farmers Association; small-scale farmers discovered that it was
  prudent to follow the dictates of the Association. Another problem faced the farmers: Somewhere in the neighborhood of 175,000
  workers were needed at the peak of the harvest season in California, but most
  of these were not needed during the rest of the year. Coupled with the influx of migrants from the
  Southwest, these facts established the conflicts between the workers and the
  land owners that Steinbeck portrays in his novel.</p>
<p><strong>Migration Patterns</strong></p>
<p>Steinbeck's story of the Joads'
  exodus from Oklahoma contains some truth. But, unstated in the novel is the fact that farmers and others had been
  leaving Oklahoma, Arkansas, and Texas since World War I, and continued to leave
  during the 1940s; the Dust Bowl did not create a sudden exit from the
  area. For example, as many Southwesterners travelled to California <em>in the 1920s</em> (when rainfall was
  plentiful) as migrated during the dirty thirties, when drought supposedly drove
  off the Joads (about 250,000). <em>Twice
  as many Southwestern migrants</em>, or nearly one-half million, went to
  California in the 1940s, when the economy was much better in the plains states
  than it had been in the 1930s. Thus,
  Steinbeck's emphasis on the drought, while partially correct (indeed, the
  migrants of the 1930s were slightly more poor than the migrants of the 1920s
  because the drought had made them more destitute), is also not totally accurate. Most migrants from the Southwest in the
  1930s, moreover, did not travel very far at all; most moved only to the next
  town or next county. Between 1935 and 1940, Oklahoma lost 309,000 migrants; 142,000 did not
  go far (no farther than contiguous states); 167,000 traveled farther, but not
  all of those went to California.</p>
<p>Other factors pulled the Okies to
  California in the 1920s, 1930s, and 1940s. The pull factors were strong and long in the making. California had begun in the 1920s to
  advertise its many attributes--jobs, beautiful scenery, chances to start anew, and, significantly, a mecca for
  vacationing Americans. California
  agriculture needed workers in the 1920s; farmers often paid migrants' railway
  fares to lure them west. And, as with
  all migrating populations in American history, many returned home and extolled
  the virtues of their new land. In fact,
  farmers did not make up a majority of migrants to California in the 1930s. Only 43 percent were farmers, with 21 percent
  being white-collar/professionals, 14 percent unskilled/domestic laborers, 13
  percent semi-skilled/service workers, and 8 percent proprietors or
  professionals. Thus, Connie and Rose of
  Sharon's dreams represented the majority of the migrants' dreams.</p>
<p>Given that the novel was very much a "road novel," were its
  depictions accurate? The Joads appear to have been rather slow travelers, taking
  several months to make the trek of 1,200 miles. Migrants who traveled to California took much less time to do so than
  did the Joads. The average length of a trip from Oklahoma to California, with a decent
  car, was only three or four days. In
  addition, the Joads did not represent a typical
  migrant family. The average Southwestern
  migrant family consisted of 4.4 members. The Joads did not reflect the average ages of
  migrants, either. Out of the twelve
  people in the Joad family, five were over 40. But 60 percent of most migrants were under
  35.</p>
<p>Although Steinbeck did not include in the novel allusions to
  other groups of migrants, he was well aware of the history of migration in
  America and in California. </p>
