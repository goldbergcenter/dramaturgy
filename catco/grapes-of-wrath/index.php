<h1><a name="America_in_the_1930s"></a>America in the 1930s</h1>
<p>In
  hindsight, the strong and sharply divided reaction to the publication of <em>The Grapes of Wrath</em> in 1939, and to the
  film adaptation in 1940, is not surprising. Steinbeck's artistic achievement tapped into and embodied some
  significant cultural transformations that had been occurring in the United
  States for over a generation. The novel
  also joined a plethora of criticisms of the changing political economy. And it challenged some American beliefs and
  new cultural trends as well as reaffirmed long-held American values.</p>
<p><strong>The Political-Economic Context</strong> </p>
<p>Historians who study the Great Depression, alas, do not agree
  on what caused the horrendous economic downturn, which affected most of the
  world. Some emphasize monetary policy
  (the increase or decrease in the money supply); others suggest that a major
  change in the structure of the economy brought on the downturn (similar to
  recent debates about "old" and "new" economies). Generally speaking, however, we can point to
  some significant facts and trends. By
  the late 1920s, Americans were producing more industrial and agricultural goods
  than they were consuming. By 1933, one
  out of four Americans who wanted to work could not find jobs; "service"
  enterprises like fast foods, laundries, and motels did not constitute a
  significant portion of the economy to which the unemployed could retreat. Throughout America, "Hoovervilles"
  sprung up in urban and rural areas. Named after President Herbert Hoover, who many blamed for the downturn,
  these shanty towns constructed of playboard and
  cardboard housed many of the unemployed. Many of these turned their pants pockets inside-out, which were in turn
  called "Hoover flags." </p>
<p>While there was lots of political conflict across the land, and
  some localized violence here-and-there (labor-management conflicts, farmers
  fighting sheriffs who were selling neighbors' farms, California farmers
  mistreating migrants), the fact remains that Americans' responses to the Great
  Depression did not include widespread violence and attempts to overthrow the
  government. Instead, Americans hotly
  debated the issues and competed in the political arena to have the right to
  determine appropriate responses. For
  example, thousands of unemployed veterans congregated in Washington, D.C., in
  the spring of 1932 to demand that the government give them the bonus Congress
  had authorized for them to receive in their old age (in the 1940s). President Hoover ordered the protesters
  removed in a peaceful manner, but unfortunately, the officer in charge, Douglas
  MacArthur, exceeded his orders and many of the Bonus Army were injured. President Franklin D.
  Roosevelt, who took office in March 1933, did not agree with the veterans
  either, but he treated them more humanely than had his predecessor's
  administration. Beginning in March 1933,
  and employing many of Hoover's ideas, Roosevelt and the Democratic Party worked
  hard to stem the economic downturn. Their efforts over the next six or so years boosted the American spirit
  and strengthened the Democratic Party, but their so-called "New Deal" never did
  bring about economic recovery; only mobilization for World War II brought back
  full employment.</p>
<p>Nonetheless, we must remember that if one-in-four were jobless,
  three-in-four had jobs (by the late 1930s, unemployment remained at a high 17
  percent). Many workers took pay cuts
  (President Roosevelt ordered a 10 percent pay cut for Federal workers shortly
  after taking office in March 1933), and many others were underemployed, but
  they held jobs. While the Joad family lumbered across the desert in search of work,
  others motored across that same desert to enjoy family vacations in California
  and other areas of the American Southwest. (My maternal grandfather, for example, was a
  government employee at the San Diego Navy Yard and he began a rather expensive
  hobby around 1936--home movie production). For the majority of Americans, the Great
  Depression was an inconvenience that occasioned lots of fear--would they be
  next to lose their jobs? would the government be up to
  the task of preventing revolution?--but it was not the experience for them that
  it was for those like the Joads, who we shall learn
  below, represented a very small percentage of the American population.</p>
<p>The plight of the unemployed not only attracted the attention
  of the New Dealers, but it also joined with other cultural transformations to
  focus the majority of Americans' attention on something they had not seen much
  of before: the marginal or common man in
  society. This discovery had profound
  implications for the culture and the politics of the American nation and set
  the stage for stormy reactions to the publication of <em>The Grapes of Wrath</em>.</p>
<p><strong>Cultural Transformations</strong> </p>
<p>Americans had discovered "culture" in the 1920s, and by the
  Depression decade, the attempt to define "the American way of life" in cultural
  terms was well underway. Several forces
  introduced Americans to the concept of culture. America's growing prominence in the world arena, the emergence of the
  social sciences, particularly anthropology, the spread of new media
  (photographs, film, radio, mass circulation magazines like <em>Time</em> and <em>Life</em>), and the
  resurgence of an old medium (books! for Americans began to read more
  non-fiction than fiction in the 1930s) prompted Americans to look at themselves
  in ways that they had never done before. Two anthropological works, Stuart Chase's <em>Mexico: A Study of Two Americas</em> (1931) and Ruth Benedict's <em>Patterns of Culture</em> (1934) strongly influenced American views
  towards culture. So too did historical
  romance novels, with Margaret Mitchell's <em>Gone
  With the Wind</em> (1936) leading the way, introduce Americans to other ways of
  life (in Mitchell's case, the American South, which even today remains somewhat
  mysterious to many Americans). In the
  words of American cultural historian Warren I. Susman,</p>
<p> Americans began ... thinking in terms of patterns of
  behavior and belief, values and life-styles, symbols and meanings. It was during this period that we find, for
  the first time, frequent reference to an 'American Way of Life.' The phrase 'The American Dream' came into
  common use; it meant something shared collectively by all Americans; yet
  something different than the vision of an American Mission, the function of the
  organized nation itself. (<em>Culture as
  History</em>, 154)</p>
<p>In one sense, Americans were trying
  to define their civilization in relation to the other great civilizations of
  history.  And, rather than reject the "marginal man" in their midst,
  Americans began to incorporate him--and her--into their cultural
  definition of "American."</p>
<p>Steinbeck's <em>The Grapes of
  Wrath</em> tapped both an old and new sense of being American. As Susman has
  argued, "it is a novel of the enforced wanderings of marginal men with a
  difference, in fact with several crucial differences. Marginal man here was not alone; the strength
  and power of the family as a unit went with him. ... he shared with
  other travelers ... common purpose ... destiny." (170-171) Indeed, while American artists in the
  1920s and 1930s (John Dos Passos, Steinbeck,
  photographers) focused on the "marginal" or "common man," they did so for an
  audience that represented the majority of Americans--the middle class. And Americans embraced the "folk" American in
  mythic terms, identified with the "folk" as part of the American
  tradition. This sense of many different
  kinds of Americans belonging together was very strong by the 1930s.</p>
<p>The diversity theme emerging in the 1930s was found also in the
  nation's music. "Folk" music became
  especially important, but other musical styles co-evolved with folk music,
  including country swing, jazz (which some see as <em>the</em> indigenous music of the U.S.), big band, and blues. For the migrants in California who played
  music, "The minstrel stage, tin pan alley, early country, and cowboy music were
  all popular music sources that fed the performers' repertoires." [http://lcweb2.loc.gov/ammem/afctshtml/tsme.html]
  The migrants liked especially the Carter Family, Jimmy Rodgers, and Gene
  Autry. The migrants'
  music--that which they listened to and that which they created--included themes
  of hardship and disappointment and the desire to return home.</p>
<p><strong>New Deal and the Common Man</strong> </p>
<p>Political change underscored the cultural change. The Franklin Roosevelt-led New Deal
  Democrats, which took over the reins of government from the Republicans, who
  had maintained for a generation control of the government, utilized the mass
  media to promote their politics and American values, including belief in the
  "common man" and the power of technology to solve any social problems. For example, they hired the balladeer Woodie Guthrie to write songs that reflected the "common
  man" in America and how the common man contributed to the strength of the
  nation. Later in the decade, government
  agents fanned out across the land to record the folk music of America. And they influenced Hollywood and even hired
  filmmakers to produce films touting the American way of life.</p>
<p>New Deal Democrats also constructed institutions intended to
  structurally change the political economy in order to combat the Depression and
  prevent another one from occurring. <em>The New Deal did not bring the economy out
  of the Depression; only mobilization for World War II did that.</em> Nonetheless, the New Deal Democrats were
  successful in three areas. First, the
  various relief programs lifted the spirits of the American people simply by
  showing them that someone cared. Second,
  they reformed the political economy so that another great depression would not
  occur. They established a plethora of
  "alphabet agencies" to regulate particular sectors of the economy (banking,
  stock market, transportation, agriculture, etc.). </p>
<p>Third, the New Dealers mixed their economic programs with
  cultural ones that focused on the "common man." For example, the Works Progress Administration (1935ff) included not
  only relief and work-relief programs for the unemployed laborer, but also
  cultural programs (a theatre project, Post Office murals, jobs for writers,
  etc.) for unemployed artists. The New
  Deal included the Resettlement Administration (RA, 1935-1937) and then the Farm
  Security Administration (FSA, 1937ff) to help displaced farmers deal with the
  economic changes that so concerned Steinbeck. But the RA and FSA ran into cultural traditions (which Steinbeck alludes
  to) that made it difficult for the reformers to convince those being reformed
  to follow the new cultural values. Coupled with the political thrust of New Deal initiatives, these
  institutions underscored the American quest for cultural definition and led to
  Americans discovering that their culture, their civilization, was very
  different from others.</p>
<p>In the 1930s, Americans discovered the heritage not only of the
  various ethnicities of the European immigrants who had helped America
  industrialize, but also of Native Americans and, more slowly, (prompted in part
  by the New Deal-inspired WPA State Guidebooks) that of African Americans (who
  became by 1936 an important part of the New Deal Democrat coalition). Marginal or minority groups now mattered
  (Hispanics and Asians were still excluded), not only at the ballot box (which
  the Democrats exploited well), but also in the culture at large; strength
  through diversity, reflected in the Democratic Party's majority, was becoming a
  recognized American cultural trait. If
  the New Deal programs did not bring about economic recovery, they did help
  Americans see themselves as made up of diverse peoples.</p>
<p><strong>Critics of the New Deal</strong></p>
<p>Not all Americans supported the New Deal programs.</p>
<p>For some, the Democrats were doing too much to alter the
  economy and help the common man. In
  1934, with executives of key corporations in the lead, the Liberty League began
  a campaign that painted the New Deal as worse than fascism or communism. For the most part, the Liberty League did not
  matter all that much until later in the decade, when more conservatives
  (Democrats from the South and Republicans) began to fight the perceived
  nationalization of politics that they alleged the New Dealers had created. (Actually, most of the major New Deal laws
  and agencies included a role for the states; Roosevelt, for the most part--the
  Court Packing scheme was an exception--promoted "federalism," not
  nationalization.)</p>
<p>Most critics of Roosevelt and the New Deal, however, argued
  that the New Deal was not doing enough to help the common man deal with the
  economic depression. These critics
  mattered much more than the Liberty League, and they
  pressed President Roosevelt to expand the Federal government more than he
  wanted to. These "critics on the left"
  included Senator Huey Long (D-La) and Father Charles E. Coughlin, and, in
  Steinbeck's California, Francis E. Townsend and Upton Sinclair. Huey Long (who President Roosevelt once labeled
  one of the two most dangerous men in America), promoted his own political
  fortunes through the "Share Our Wealth" program. Long, who as governor of Louisiana in the
  1920s had elevated that state's infrastructure (schools, highways) to aid all
  citizens of the state, argued that the government should make sure that every
  American had a middle class life. To do
  this, he advocated liquidating all personal fortunes over $3 million dollars
  and distributing that money to all Americans. Father Charles E. Coughlin, the "radio priest" from Michigan, exhorted
  millions of Americans to demand social justice for all and less power for the
  private sector banks and corporations. While initially drawing from the Catholic Church's long tradition of
  promoting social justice, Coughlin eventually turned to anti-Semitism to
  promote his political agendas. Finally,
  in 1942, the Church ordered him to cease his political activities, and he
  did. Significantly, both Long and
  Coughlin tended to attract members of the middle class--those who had not
  succumbed to the ravages of the Depression but who feared that they might one
  day be out of a job. (These were
  Steinbeck's audience as well.)</p>
<p>California in the 1930s seemed especially affected by political
  movements. Francis Townsend was
  different from Long and Coughlin in that he concentrated on one interest
  group--the elderly--and proposed a plan to help them and the nation's
  economy. While overly expensive and
  perhaps too bureaucratically complex to administer, his plan (each person over
  65 was to be given $200 a month on two conditions--they quit their jobs and
  spend every penny of the monthly stipend) held some merit with millions of
  Americans. A more modest New Deal
  program, the Social Security Act of 1935, rendered Townsend's political
  movement moot. Upton Sinclair, on the
  other hand, focused his energies on eliminating all poverty in the state. Persuaded to change his party affiliation
  from Socialist to Democrat, Sinclair ran for governor of California in
  1934. He established EPIC--End Poverty
  in California--as his basic platform. It
  directly challenged the capitalistic, private property basis of the state's
  economy: The state would take over idle
  factories and fields and allow the unemployed to produce their own food and goods
  for their own use. Sinclair and his
  followers, in effect, were saying that Roosevelt's New Deal had simply not gone
  far enough. Having won the Democratic
  primary handily, Sinclair faced in the general election a well-orchestrated
  campaign of distortions, lies, and dirty tricks, funded by California
  conservative business leaders and supported by Hollywood studios and
  actors. The old economic system endured
  in California.</p>
<p>In sum, then, <em>The Grapes
  of Wrath</em> appeared at a time in which Americans were receptive to stories of
  "other cultures" and particularly attracted to the "common man"; many were
  fearful that they, too, might succumb to the economic changes occurring; many
  were suspicious of the large-scale corporations that had funded the Liberty League
  and the attack on Upton Sinclair; and, a majority of Americans voting in the
  1934 and 1936 elections demanded that the Federal government do more than it
  had been for the down and out among them.</p>
<p>Steinbeck's novel also reflected another cultural movement in
  the U.S. that did much to focus Americans on the common man.</p>
