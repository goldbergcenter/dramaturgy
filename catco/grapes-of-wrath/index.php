<p><strong>Props</strong></p>
<p><strong>Elizabeth Langford</strong></p>
<p>The biggest responsibility of the properties
  master is to be as realistic and historically accurate as possible. The
  challenge, as is normal for any period play, is finding items currently
  available that are close enough to what was used "at that time"
  (e.g., cotton clothes line, wooden clothes pins or the red rags gas station
  attendants used to use). Conveying the desperation these people went through
  and the fact that they were not always destitute, that they had been prosperous
  before they lost their farm, and that they were proud were key goals to meet.
  Their belongings should show extreme overuse, worn down by the elements; but
  they also needed to look as if they were nice things at one time in the past.
  Magazines of the day (<em>Saturday Evening Post</em>,<em> Life</em>,<em> Look</em>,<em> Good Housekeeping</em>) and photographs from the Dust Bowl (especially by
  Dorothea Lange) were particularly helpful in revealing the types of things
  people used in the 1930s--what kinds of dishes, pots and pans, baskets,
  appliances of any type, what products were available (i.e. Morton Tender- Quick
  for salting the pork, Havoline oil used by the gas
  station attendant). Photos of the fleeing Okies in
  their trucks loaded down with all of their worldly possessions were
  particularly helpful in "loading" the Joad truck. The trick, given the limited amount of space and the number of people
  needing to be in that space, was to make it look as if the Joads took everything but the kitchen sink.</p>
<p>Shopping the antique/vintage shops and thrift
  stores usually turns up many treasures.</p>
<p>As with the costumes, items purchased new must
  be distressed. For example, the plates for "supper" were new pie
  plates. I sprayed them down with black spray paint first, then used a
  combination of other colors of paint for texture and finally gave them the
  "well-worn" look with a hammer. The slabs of "salt pork"
  were made using several layers of foam of different thickness, modeling clay,
  and a lot of glue.</p>
<p>In creating the world of the Joads and the other displaced people they meet on their trip to California, as well
  as the migrant life in California, the use and re-use of all props and set
  pieces to build the truck and define the locations (Hooverville, Weedpatch Camp, etc ) is
  reminiscent of the migrants' use and re-use of items, sometimes to the point of
  wearing them out.</p>
