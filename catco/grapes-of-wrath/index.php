<h1><a name="The_Film"></a>The Film</h1>
<p>Both John Steinbeck and John Ford fell within the emerging
  documentary tradition of the 1930s, but each chose to employ documentarian
  approaches in different directions. Steinbeck chose to place the Joads within a
  penetrating vision of a larger family of humans, indeed within the larger
  natural world that humans shared with the land, vegetation, and animals. John Ford chose to narrow the documentary
  vision to emphasize the <em>Joad</em><em> family</em>; in so doing, he left out the
  larger connections between the Joads and the land and
  the Joads and society. Ford also encouraged the actors to
  overdramatize the words, which made them less plausible as real, everyday
  people. A rather haphazard movie studio
  system lay behind the production of the film, which explains in part the poor
  results. (That same system, or course,
  accidentally produced the contemporary classic <em>Casablanca</em>). Despite the
  media and Hollywood attention given the film, it is a poor substitute for the
  novel or even Galati's adaptation.</p>
