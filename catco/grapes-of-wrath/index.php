<h1><a name="Terms_of_Interest"></a>Terms of Interest</h1>
<p><strong> Bulls: </strong>Private, but sometimes public, policemen that
  the California farmers and town officials hired to run-off migrants from the
  areas. </p>
<p><strong> Hooverville:</strong> These were camps that
  migrants and unemployed established on the road and in American cities. They were named after President Herbert
  Hoover, whom many blamed for the Great Depression.</p>
