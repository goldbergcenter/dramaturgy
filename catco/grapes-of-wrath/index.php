<h1><a name="John_Steinbeck"></a>John Steinbeck</h1>
<p><img class="portrait" src="images/john-steinbeck.jpg" width="111" height="141" alt="John Steinbeck">
  Born in 1902, John Steinbeck remained in the Salinas Valley area of California
  until he went to Stanford in 1919, where he took writing courses. Exhibiting a tendency to fall behind in his
  studies, Steinbeck did not graduate. The
  Monterey peninsula was his favorite community, and from his experiences there,
  he wrote much of his best work. He left
  California for the first time at age 25, when he went to New York City as a
  journalist and sometime laborer. He
  returned to California, working for awhile as a caretaker of a Lake Tahoe
  estate, and in 1929, published his first novel, <em>Cup of Gold</em>. Two novels and
  short story writing barely sustained him until his first major success in 1935,
  the publication of <em>Tortilla Flat</em>. During the late 1930s, Steinbeck focused on
  California's white laboring class, publishing <em>In Dubious Battle</em> (1936), <em>Of
  Mice and Men</em> (1937), and <em>The Grapes
  of Wrath</em> (1939), which won in 1940 both a Pulitzer Prize and the Award of
  the American Booksellers Association, which is now called the National Book
  Award. He entered the filmmaking
  business (<em>The Forgotten Village</em>,
  1941) and supported the war effort as a journalist and author (<em>Bombs Away</em>, 1942). </p>
<p>Although his literary reputation waned somewhat after 1939,
  Steinbeck continued to publish regularly, even as he traveled widely around the
  world (he returned to California only occasionally in the 1940s): <em>Cannery
  Row</em> (1945), <em>The Wayward Bus</em> (1947), <em>The Pearl</em> (1947), <em>A Russian Journal</em> (1948), <em>Burning Bright</em> (1950), <em>The Log from the Sea of Cortez</em> (1951),
  and <em>East of Eden</em> (1952). In the 1950s, New York and Sag Harbor became
  the home of Steinbeck and his third wife, Elaine, a native of Texas, until his
  death in 1968. His <em>Travels with Charley in Search of America</em> (1962) revealed a man
  somewhat unsettled with the changes occurring in America, particularly the loss
  of community that he had grown up with in Monterey. In 1962, Steinbeck won the Nobel Prize. He died in 1968, within a year of returning from
  a trip to Vietnam (which President Lyndon B. Johnson had asked him to make).</p>
<p>John Steinbeck had many friends, some of them rather prominent
  in artistic circles. Indeed, his friends
  and the strain of buying a new ranch house interfered with his writing <em>Grapes of Wrath</em> between June and October
  1938. Visitors included Lorentz and
  Charles Chaplin. His first wife, Carol,
  was his sounding board; he trusted her judgment implicitly and completely. He also had a good relationship with his
  editor and publisher (helping the latter out of bankruptcy when he agreed to go
  to Viking Press, but only if his old publisher was given a job).</p>
<p>Two important individuals who influenced Steinbeck in the 1930s
  were Pare Lorentz, noted above, and Tom Collins, manager of a government
  migrant camp. It was from Collins that
  Steinbeck received--even while he was writing the novel--important gritty
  details of the migrant camp experience.</p>

