<h1><a name="The_Novel"></a>The Novel</h1>
<p><strong>History</strong></p>
<p>The publication of <em>Grapes
  of Wrath</em> in 1939 created, as Steinbeck predicted, a firestorm of
  controversy. Several cities in the U.S.
  banned the book because the sexual content and "leftist" views offended some
  citizens. The novel sold more copies
  than any other contemporary work, except <em>Gone
  with the Wind</em> (it was a best seller in Oklahoma). Politicians in California and Oklahoma
  criticized Steinbeck's emotionalism, charged him with misrepresenting the
  facts, and accused him of supporting "anti-American" values. Others praised Steinbeck for being the
  conscience of America. Certainly, for
  its content and style and for the responses it elicited, <em>The Grapes of Wrath</em> must be considered as one of the most important
  works in all of American literature.</p>
<p>Indeed, this novel represented Steinbeck's third attempt
  to write the story of the migrants in Depression America. <em>In
  Dubious Battle</em> (1936) dealt with striking California migrant farm workers,
  but it cynically concluded that communist leaders organized only to serve the
  party, not oppressed individuals, and that individuals were incapable of
  organizing collectively against oppression. The next effort Steinbeck burned: <em>L'Affaire</em><em> Lettuceberg</em>,
  in his own words, was a "mean, nasty book." But writing it allowed Steinbeck to get the hate out of his system so
  that he could produce the masterpiece.</p>
<p><em>The Grapes of Wrath</em> represented Steinbeck's final effort to explore the reasons behind and the
  possible responses to the failure of American society that the plight of the
  migrants reflected. That the migrants
  existed at all in a country based on the worship of and the achievements in
  material progress framed the story in terms of failure. There was a loss of spirit or hollowness in
  the plight of the Joads ('that's not supposed to
  happen in America'). Steinbeck wondered
  if the Joads--and the American people--could rise to
  the challenge.</p>
<p>The novel had an important impact on other social
  commentators. Dorothea Lange, infamous
  photographer for the Farm Security Administration, had traveled about the
  South, Southwest, and California documenting life of America's poor even before
  the novel was published. After
  Steinbeck's masterpiece came out, she went looking for migrants who reflected
  the Joads and she found them. Perhaps her most famous photo, "Migrant
  Mother," was in fact one of half a dozen photos she took of the same woman; the
  two of them experimented until they got the "look" that each wanted; emotion
  more than fact or reality ruled Lange's photography.</p>
<p><strong>Structure, Meanings, and Influences</strong></p>
<p>Good fiction fractures reality and puts it back together in
  such a way as to lead the reader to a comprehension of central truths and
  realities that were hidden from everyday life.</p>
<p>Three perspectives frame the novel: <strong>the
  economic</strong> (agriculture, technology, banks, management/labor); <strong>the social</strong> (how individuals behave
  alone and in groups; how they respond or not to change); and, <strong>the philosophical</strong> (and/or religious;
  how Steinbeck relates human experience to the larger natural view of the
  world). While through each of these
  perspectives universal truths and realities are revealed, the American
  setting of the novel reveals as well specific American truths and
  realities.</p>
<p>The novel is broken into three chronological narratives: </p>
<p>(1) Chapters 1 - 11 focus on the land, the
  drought and dust, and why the Joads left; </p>
<p>(2) Chapters 12 - 18 present the migration or
  exodus of the Joad family to the promised land of
  California; </p>
<p>(3) Chapters 19-30 present the sojourn in
  California, Jim Casy's death and Tom Joad's decision to carry on his fight, and Rose of Sharon's
  unselfish act.</p>
<p>Steinbeck interspersed intercalary (interconnecting) chapters
  (a technique he borrowed from John Dos Passos, who
  had great success with his <em>USA</em> trilogy in the 1920s and 1930s). These
  "generalist" chapters offered the reader the broader story within which the Joad family's saga took place.</p>
<p class=MsoBodyTextIndent>Steinbeck's novel clearly
  reflects religious inspirations and other American cultural themes and
  myths. The Joads represent the chosen people, the Iraelites,
  Americans. The preacher, Jim Casy, just like Jesus Christ (both have the same initials),
  wanders for awhile, thinking, and rejects old religion for a new one;
  sacrifices himself for others unselfishly; tells his murderers they know not
  what they are doing; inspires others (Tom) to carry on the fight. Rose of Sharon's unselfish act at the end of
  the story represents the resurrection of Christ. The Bible contains numerous references to
  "grapes" and "wrath." In Numbers 13:23, grapes
  represent abundance; in Deuteronomy 32:32, grapes represent wrath and
  vengeance. And, of course, Julia Ward
  Howe's song, "Battle Hymn of the Republic," relates to Revelation 14:19--"the
  great winepress of the wrath of God." The phrase in the song, "Let the Hero, born of woman, crush the serpent
  with his heel," clearly is reflected in the last scene of the novel, if one
  sees the serpent as big business agriculture/capitalism, which had manipulated
  the Joads, the starving man and boy, and all migrants. Fittingly, perhaps, it was Steinbeck's wife
  Carol who came up with the title, after hearing the song.</p>
<p class=MsoBodyTextIndent>    Steinbeck re the title:
  "...I like it better all the time.  I think it is Carol's best title
  so far.  I like it because it is a march and this book is a kind of march--because ot is in our own
  revolutionary tradition..."  (<em>Working Days</em>,
  p. 169).</p>
<p>The novel reflects many American traits as well, although the
  way Steinbeck portrays them, they too are about morality and the
  spiritual. The plight of the Joads represents the loss of Jeffersonian values of
  attachment to the land, small-scale farming, individualism, and freedom. Absentee landowners led to erosion of
  morality (tractors tearing down homes); loss of land led to human erosion (the Joad family breaks up). Steinbeck's progressive, liberal politics (not communism, which he
  detested) infuse the novel: Large-scale
  institutions (the Shawnee Land and Cattle Company, the banks, the California
  Farmers' Association) oppress the people; the people themselves hold the key to
  change, but not through individualism or self-reliance; only by banding
  together can they throw off the oppressive institutions (the smooth operation
  of the Weedpatch Government camp operations shows
  this very clearly). Steinbeck was not
  calling for outright revolution, but rather for enlightened responses to the
  problems. This "progressive" or
  "liberal" politics, which emerged at the end of the 19th century and the
  beginning of the 20th century and was exemplified on the national level by
  President Theodore Roosevelt, held within it a message to the wealthy: Reform the unintended and unwanted
  consequences of industrial capitalism or face more radical upheavals within
  society. First Jim Casy,
  then Tom Joad becomes the messenger; by rallying
  others to organize, they will raise their voices to a volume that will demand a
  response.</p>
<p><em>Steinbeck
  on Tom: "... Tom - half bitterness, half humane. ... Escapes in the night. Hunted, hunted. Over the last
  pages Tom hangs like a spirit around the camp." </em>(Entry #87,<em> Working Days</em>).</p>
<p>While Tom is clearly an important character (and the essence of
  the novel's main thrust for adapter Frank Galati), Ma Joad is arguably as important a character. Steinbeck was very interested in the evolutionary change from
  matriarchal to patriarchal societies, as outlined by anthropologist Robert Briffault in <em>The
  Mothers: The Matriarchal Theory of Social Origins</em> (New York, 1931). Steinbeck, according to his wife Carol,
  created Ma from Briffault's ideas. In the matriarchal society, the sense of
  belonging to a group revolved around the woman; her brothers often performed
  the duties that in later patriarchal societies the husband and father did. Equality and egalitarianism marked
  matriarchal societies. Because women worked
  hard at various domestic labors (which included directing the economic pursuits
  of the household industries such as tanning, potting, building the home, and
  tool-making), they achieved status and influence within the clan. With the rise of industrialism, however, and
  an attendant emphasis on sexual relations, men gained power and established
  patriarchies. Economic wealth, based on
  individualism and land ownership, overtook the more communal approach of
  matriarchal societies.</p>
<p>But Steinbeck shows (through the characters of Grandpa and Pa Joad particularly) that the patriarchal society, based as
  it was on independence, individualism, and land owning, was no longer capable
  of surviving within the emerging 20th century economy. Beliefs in individualism and private property
  had blinded the men to the fact that large scale corporations held an economic
  advantage over the individual farm owner. <em>Perhaps</em>, Steinbeck suggests
  through the character of Ma Joad, a return to a
  matriarchal society was what Americans needed to do in order to respond to the
  economic situation of the 1930s.</p>
<p>Thus, Ma Joad's matriarchal society
  dovetails with the insights of Jim Casy ("I to we") to suggest that the solution to the problems of the
  migrants rested, at least in part, on the migrants joining together to more
  effectively react to the new economic realities. It is Ma's influence on Tom, as much as Jim Casy's influence, that forces Tom
  to trade-in his male-oriented individualism for a more matriarchal emphasis on
  working together. Adding to the dramatic
  tension, moreover, is the insight that Ma herself does not understand how she
  has influenced Tom; <em>her</em> focus is on
  her family, while her example has shown Tom that a larger family--the migrant
  workers--should be <em>his</em> focus.</p>
<p><em> Steinbeck on Ma: "And I want to build her up as much as possible. Her possibility of an
  organized society. I want to show
  how valuable Ma is to society--what a waste there is." (Entry #68, Working Days)</em></p>
<p><em>"Today
  Ma meets the ladies committee and it must have some charm. It must. It can't flop because here is the great contrast. Here is the tremendous contrast. It must be charming." [Entry #69, Working Days]</em></p>
<p>One other theme is noteworthy (indeed, it caused much
  criticism): Steinbeck constantly put the Joads and the larger story within a larger natural
  world. The sexual antics of Granpa, the turtle (which does not appear in the play), the
  land itself--all of these indicates Steinbeck's belief that humans were only
  part of a larger universe; their actions--like farming the Great Plains--had
  consequences elsewhere in the natural world, just like the actions of uncaring
  large-scale corporations had consequences on families and individuals.</p>
<p><strong>Is the novel representative of history?</strong></p>
<p>Within two months of the novel's publication, Marshal V. Hartsanft published <em>Grapes
  of Gladness: California's Refreshing and
  Inspiring Answers to John Steinbeck's Grapes of Wrath</em>. In this rejoinder, a migrant family is
  welcomed with open arms to California. In Thormis Miron, <em>The Truth About John Steinbeck and the Migrants</em> (1939), the author posed as a migrant,
  averaged $4 per day, and was asked by the growers to stay on. Steinbeck knew these sorts of attacks were
  forthcoming; but his own experiences gave him the strength to move forward. On the other hand, investigators for Darrell
  Zanuck, who produced the John Ford film of the novel, found conditions to be
  worse than Steinbeck had depicted.</p>
<p>To appreciate how true the novel was, we need to understand the
  relationships between history and fiction, the facts behind the Dust Bowl and
  agriculture in California, the migration patterns of Southwesterners over time, and migrant life. Generally
  speaking, the last half of the novel, the scenes in California, is more
  accurate than the first half; but a certain authenticity still pervades the
  entire novel.</p>
<p>The work is fiction, not history, in the sense that the
  characters are not "real," nor are the various stories factually true. But Steinbeck did not create the characters
  and the plot lines solely from his imagination. He drew from his experiences while traveling in Oklahoma and along Route
  66, but mostly from living in California. As noted above, the government agent, Tom Collins, sent Steinbeck
  statistics from the camp he superintended. But Steinbeck also experienced the migrant camps firsthand (living and
  working in one for several weeks) and was repulsed by what he discovered. In fact, the last scenes representing the
  flood reflected real events that Steinbeck witnessed in February 1938 in
  Visalia and Nipomo, California; arguably, this horrific reality was the final
  push that enabled Steinbeck to produce the masterpiece he had been trying to
  create for several years.</p>
<p>Steinbeck did not attempt to be evenhanded and he made some
  small mistakes. He focused narrowly on
  agriculture and left out other contemporary aspects of the Bear Flag State,
  such as its tremendous urban growth, especially in the southern half of the
  state, and the fact that Mexican and Asian migrants also lived in California. (Indeed, the Mexicans and Asian migrants, who
  had been there first, gave way before the quarter million or so "Okies" who trekked to California in the late 1930s. Once mobilization for war stimulated the
  economy, however, California growers petitioned the government to bring back
  the Mexican migrants.) Some errors do
  appear in the novel. Sallisaw, Oklahoma,
  is located near the Arkansas border, nearly 400 miles to the east of where the
  Dust Bowl actually was, and it lay in a grape growing region. Large corporate farms existed in California
  in the 1930s, but had not yet overtaken the Sooner state.</p>
<p>Nonetheless, mechanization of agriculture, particularly cotton
  production, had advanced the most in the area of the Dust Bowl; 40 percent of
  the farmers, like the Joads, were tenants. Most of the so-called Okies did not leave because of the dust, but rather because they had long since lost
  the ability to make a living off the land. They--not the corporate farms--had worn out the land. (Ironically, New Deal agricultural programs
  pushed even more tenant farmers off the land: In an attempt to control production, the government paid farmers not to
  plant; landowners took the tenant farmers' lands out of production, told them
  to leave, received money from the government for doing so, and continued to
  farm their own land.)</p>





