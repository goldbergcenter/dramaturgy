  history of California's importation and treatment of foreign labor is a
  disgraceful picture of greed and cruelty. </em>[The Chinese were followed by the Japanese, Mexicans, and
  Filipinos.]</p>
<p><em>Foreign
  labor is on the wane in California, and the future farm workers are to be white
  and American. This fact must be
  recognized and a rearrangement of the attitude toward and treatment of migrant
  labor must be achieved.</em> (<em>Harvest Gypsies</em>, pp. 52, 57).</p>

