  <p>For every
    production, CATCO hires a dramaturg to collect
    information about the play (its structure and setting), the playwright, and
    past productions.  This information,
    which is often put together into an "Actors' Book," aids the director in establishing
    an approach and it helps the actors prepare for their different
    characterizations.  It is supplemental
    information that furnishes facts and context, for the director, actors,
    properties master, music director, and costume, scenic, lighting, and sound designers, <strong>always</strong> do their own research as well.  The key
    word is "collaboration"; everyone collaborates and shares information with
    everyone else.  In the case of "<em>You're My Boy</em>", an original production, the playwright Herb
  </p>
